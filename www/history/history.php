  <p>
    Founded in 1966 by the immortal David Greenberg, Chief Centurian Number I, the Warriors 
    Band has become THE dominant musical force at Waterloo sporting events. 
    The Band grew, under the auspices of the UW Athletic Department, from four 
    to fifty members in its first year alone.
  </p>
  <p>
    Early organizers of the Warriors Band had many grand plans for the group, most of which, 
    requiring large sums of money, remained grand plans. They did, however, manage to 
    acquire a 48" diameter Ludwig bass drum which accompanies the Band 
    on its travels throughout the world.
  </p>
  <p>
    An infusion of capital in 1980 under the tenure of Steve Hayman, CCWB IX, enabled the 
    purchase of enough instruments to keep almost everyone happy. In addition, regular 
    (informal) practices were instituted which has expanded the group's repertoire 
    to a two-digit number of pieces.
