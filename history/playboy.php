<p>
  The following repudiation was sent to big Hugh in 1969 for excluding the 
  Warrior Band from the Peoples Poll:
</p>
<hr>
<br />
<br />
<div class=left>January 22, 1969</div>
<p>
  Playboy Magazine,<br />
  Playboy Building,<br />
  919 N. Michigan Avenue,<br />
  Chicago, Ill. 60611
</p>
<p>
  Dear Playboy,
</p>
<p>
  "Why", cooed the delectable young coed, "did the Warriors Band no make the 
  Playboy Jazz Poll?"
</p>
<p>
  "Because", replied the handsome mathematics senior, obiously flustered, 
  "the jazz establishment is firmly entrenched in the Playboy Clique, thus 
  inspiring a list of only the top twenty-five artists, without any accounting 
  of votes, clearly repressing the free competition which North America 
  hypocritically still claims to cherish".
</p>
<p>
  As Chief Centurion of the aforementioned group (and the aforementioned 
  handsome math senior) I feel compelled to inform you that - 
</p>
<p>
  THE WARRIORS BAND IS ALIVE AND WELL AT THE UNIVERSITY OF WATERLOO, 
  WATERLOO, ONTARIO, CANADA (north, and a little left of Washington, D.C.).
</p>
<p>
  Enclosed you will find a picture of the group just after it had lived up to 
  its most recent rallying cry "This band will self-destruct in fourteen bars 
  of any song you want to name".
</p>
<p>
  You might be concerned that the number of people in the group would exclude 
  it from Playboy's Jazz Combo classification, but everybody says, after 
  listening to the group on CHYM Radio, Waterloo, Canada (more north and more 
  left of the Mississippi) that "there couldn't possibly be seven musicians 
  (that is, real musicians) in that band".
</p>
<p>
  In spite of the severe setback in the elitest (alitest in the sense that 
  you have to be good) 1969 Playboy Jazz Poll we shall continue with our 
  cause to promote tone-deafness and stamp out rhythm methods, until all have 
  been converted.
</p>
<p>
  I remain,<br>
  Yours anyway,
</p>
<p>
  David Greenberg<br>
  Chief Centurion<br>
  Warriors Band<br>
  University of Waterloo, Waterloo, Ontario<br>
  Canada<br>
  (a little to the north and a lot to the left of Alabama)
</p>
